#!/usr/bin/node

const body = `<html>
<head><title>Webserv CGI Node Test</title></head>
<body>
    <h1>CGI Power from Node.js!</h1>
    <p>This output was generated by a JavaScript script.</p>
    <p>Look Ma, no Express.js!</p>
</body>
</html>`;

// Headers
process.stdout.write("Content-Type: text/html\r\n");
process.stdout.write(`Content-Length: ${Buffer.byteLength(body)}\r\n`);
process.stdout.write("\r\n");

// Body
process.stdout.write(body);
